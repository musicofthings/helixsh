"""HPC Environment Modules profile generator.

Many HPC clusters use Environment Modules (or Lmod) instead of containers.
nf-core pipelines support a `modules.config` that maps process labels to
`module load` directives.  This module generates that config from helixsh's
tool database so users can run nf-core pipelines on container-less clusters.

Output format: Nextflow `modules.config` with `withName` selectors and
`module` directives — ready to be passed via `-c modules.config` or merged
into the user's `nextflow.config`.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from pathlib import Path

