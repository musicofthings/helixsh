@dataclass
class ModuleEntry:
    process_selector: str
    module_name: str
    module_version: str
    full_module: str   # e.g. "STAR/2.7.11b"


@dataclass
class EnvModulesConfig:
    entries: list[ModuleEntry] = field(default_factory=list)
    warnings: list[str] = field(default_factory=list)

    def to_nextflow_config(self) -> str:
        """Render a Nextflow modules.config string."""
        lines = [
            "// Auto-generated by helixsh envmodules-wrap",
            "// Pass to Nextflow with: -c modules.config",
            "process {",
        ]
        for entry in self.entries:
            lines.append(f"    withName: '{entry.process_selector}' {{")
            lines.append(f"        module = '{entry.full_module}'")
            lines.append("    }")
        lines.append("}")
        return "\n".join(lines) + "\n"


def list_known_modules() -> list[dict[str, str]]:
    return [
        {"tool": tool, "module": info["module"], "default_version": info["version"]}
        for tool, info in sorted(_MODULE_DB.items())
    ]


def generate_modules_config(
    tools: list[str],
    version_overrides: dict[str, str] | None = None,
    process_selector_prefix: str = "",
) -> EnvModulesConfig:
    """Generate a Nextflow modules.config for the given tool list."""
    overrides = version_overrides or {}
    config = EnvModulesConfig()

    for tool in tools:
        tool_norm = tool.strip().lower()
        if tool_norm not in _MODULE_DB:
            config.warnings.append(f"Unknown tool '{tool}' — skipped. Use --module-name to specify manually.")
            continue
        info = _MODULE_DB[tool_norm]
        version = overrides.get(tool_norm, info["version"])
        module_name = info["module"]
        full_module = f"{module_name}/{version}"

        # Default process selector: uppercase tool name, optionally prefixed
        selector = f"{process_selector_prefix}{tool_norm.upper().replace('-', '_')}"

        config.entries.append(ModuleEntry(
            process_selector=selector,
            module_name=module_name,
            module_version=version,
            full_module=full_module,
        ))

    return config


def write_modules_config(config: EnvModulesConfig, out_path: str) -> None:
    p = Path(out_path)
    p.parent.mkdir(parents=True, exist_ok=True)
    p.write_text(config.to_nextflow_config(), encoding="utf-8")
